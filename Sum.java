public class Sum {//Sum of 10 numbers

    public static void main(String[] args) {//main method
   
        int total = 0;//variable

        for (int number = 2; number <= 20; number+=2){//loop
            
            total += number;//sum of 10 numbers
            
            System.out.print("Sum is "+total+"\n");//print
        
        }//end of loop

    }//end of main method
    
}//end of class
