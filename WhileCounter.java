public class WhileCounter {// class header
    
    public static void main(String[] args) {// main method header

        int counter = 1;// declare and initialize counter

        while (counter <= 10) {// loop header

            System.out.println(counter);// print the value of counter
            
            ++counter;// increment counter

        }// end while

        System.out.println();// print new line

    }// end main

}// end class WhileCounterS