public class DoWhileTest {//do while loop
    
    public static void main(String[] args) {//main method
        
        int i = 1;//initialization
        
        do {//do while loop
            
            System.out.println(i);//print i
            
            ++i;//increment i
        
        } //end of loop
        
        while (i <= 10);//condition

        System.out.println();//new line
    
    }//end of main method

}//end of class
